/**
 * Translate a UI string key to the current Obsidian display language, falling
 * back to English for an unknown language or a key a locale hasn't translated.
 */
export const t = (key: StringKey): string => {
	const lang = (getLanguage() || "en").split("-")[0];
	return LOCALES[lang]?.[key] ?? en[key];
};
